def with_genre(base_prompt: str, genre: str | None) -> str:
    """Памятка: инструкция редактируется при особом жанре/требованиях."""
    if not genre:
        return base_prompt
    return f"{base_prompt}\n\nОСОБЫЙ ЖАНР/ТРЕБОВАНИЯ: {genre}"
