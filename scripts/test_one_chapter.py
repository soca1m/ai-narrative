"""Тест: реальная генерация ОДНОЙ адалт-главы (Claude подписка + грок OpenRouter).

Claude (подписка) пишет всю главу с меткой → грок (OpenRouter) заполняет адалт.
Итеративно добивает объём. Результат → файл.

Запуск: .venv/bin/python scripts/test_one_chapter.py
"""
from __future__ import annotations

import os
import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(ROOT / "src"))
sys.path.insert(0, str(ROOT))

from dotenv import load_dotenv  # noqa: E402
load_dotenv(str(ROOT / ".env"))

# Claude — через подписку; грок-адалт всегда через OpenRouter-ключ.
os.environ["USE_CLAUDE_SUBSCRIPTION"] = "1"

from narrative import nodes  # noqa: E402
from narrative.state import Chapter  # noqa: E402

CHARACTERS = """\
Анна Ковальски, 24 — инструктор тенниса в клубе. Внешность: высокая,
спортивная, рыжие волосы в хвосте, веснушки, зелёные глаза. Характер: упрямая,
ироничная; внутреннее противоречие — жёсткий контроль снаружи, тяга потерять
его наедине. Манера: коротко, с подколами, не сюсюкает. Тайна: спит с членом
совета клуба ради продления контракта. К Марку — соперничество и злость.

Марк Дельгадо, 29 — новый управляющий клубом, поставлен «навести порядок».
Внешность: смуглый, подтянутый, тёмная щетина, дорогие часы. Характер:
холодный, расчётливый; противоречие — под маской дисциплины азарт игрока.
Манера: ровный голос, давит паузами, не повышает тон. Цель: найти повод
уволить старый состав. К Анне — раздражение, переходящее во влечение.
"""

LOCATIONS = """\
Раздевалка инструкторов (LockerRoom): тесная, кафель, длинные скамьи, ряды
шкафчиков, запах хлорки и дезодоранта, тусклый дежурный свет вечером, скрипучая
дверь без замка. Приватно после закрытия — никого.

Корт №1 (Court): крытый, гулкий, яркий верхний свет, сетка, скамья судьи.
"""

PLAN = """\
Бит 1: вечер, клуб закрылся. Анна задержалась после тренировки на Корте №1,
вытирает ракетки. Марк находит её — пришёл с распечаткой нарушений по её
контракту, хочет прижать.
Бит 2: на корте словесная дуэль — он давит цифрами и угрозой увольнения, она
огрызается, не уступает; между ними искрит злость и явное физическое осознание
друг друга.
Бит 3: спор смещается в Раздевалку инструкторов, когда она уходит собрать вещи;
он идёт следом, не закончив разговор. Теснота, близость, оба заведены.
Бит 4 (адалт-точка): злость переходит в жёсткое взаимное притяжение — Анна и
Марк, в раздевалке, борьба за контроль становится сексом по обоюдному напору.
Бит 5: после — обоим ясно, что расклад сил изменился; ни один не признаёт вслух.
Марк уходит, забрав распечатку. Точка выбора игрока: пойти за ним и закрепить
преимущество / остаться и сохранить лицо.
"""


def main() -> int:
    state = {
        "characters": CHARACTERS,
        "locations": LOCATIONS,
        "default_words": 3600,
        "genre": "",
    }
    ch = Chapter(
        index=0,
        title="Счёт по нулям",
        plan=PLAN,
        is_adult_point=True,
        adult_note="Анна и Марк — вражда/борьба за контроль переходит в секс "
                   "в раздевалке инструкторов, по обоюдному напору",
    )
    print("Генерю главу (Claude подписка + грок OpenRouter)…", flush=True)
    t0 = time.time()
    ch = nodes.write_chapter(state, ch, 0)
    dur = time.time() - t0

    text = ch.dialogue or ""
    wc = len(text.split())
    marker_left = "[[ADULT_SCENE]]" in text
    out = ROOT / "test_chapter.txt"
    header = (
        f"# слов: {wc} | сек: {dur:.0f} | адалт_блок: {ch.adult_block_reason}\n"
        f"# статиков: {len(ch.statics)} | анимаций: {len(ch.anims)} | "
        f"метка осталась: {marker_left}\n\n"
    )
    out.write_text(header + text + "\n", encoding="utf-8")
    print(f"DONE: {wc} слов, {dur:.0f}с → {out}", flush=True)
    print(f"метка осталась: {marker_left}, адалт_блок: {ch.adult_block_reason}",
          flush=True)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
